// maximum number between two number

let a = 30, b = 45;
let result = a > b ? "a is greater" : "b is greater";
console.log(result); // b is greater

// Maximum number between three number

let x = 85, y = 56, z = 16;
let max = x > y ? (z > x ? "z is greater" : "x is greater") : "y is greater";
console.log(max); // x is greater