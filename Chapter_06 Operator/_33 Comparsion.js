// Comparision Operator - true or false - boolean value

// >, <, >=, <=, ==, ===, !, !=, !==

// = -> assignment operator
// == -> loose comparison
// === -> strict comparison

console.log(3 > 5); // false
console.log(3 < 5); // true
console.log(4 >= 4); // 4 > 4 or 4 === 4
console.log(3 <= 4); // 3 < 4 or 3 === 4