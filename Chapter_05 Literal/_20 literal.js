let number = 10; // This is a numeric literal assigned to the variable "number"
let age = "30"; // This is a string literal assigned to the variable "age"
let isLoggedIn = true; // This is a boolean literal assigned to the variable "isLoggedIn"
let pi = 3.14; // This is a numeric literal assigned to the variable "pi"
let name = "Girish"; // This is a string literal assigned to the variable "name"
let nullValue = null; // This is a null literal assigned to the variable "nullValue"
let undefinedValue = undefined; // This is an undefined literal assigned to the variable "undefined"


// typeof - operator - It will tell you what is a data type that it has
console.log(typeof number); // "number"
console.log(typeof age); // "string"
console.log(typeof isLoggedIn); // "boolean"
console.log(typeof pi); // "number"
console.log(typeof name); // "string"
console.log(typeof nullValue); // "object" (this is a known JS quirk/bug)
console.log(typeof undefinedValue); // "undefined"